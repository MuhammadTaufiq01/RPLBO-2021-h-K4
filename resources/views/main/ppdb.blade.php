<p>Setiap santri akan ditempatkan pada kelas sesuai dengan kemampuan dasar yang dimilikinya pada awal masuk pondok pesantren. Tim pesantren akan mengadakan penelusuran kemampuan/placement test yang diorientasikan dan dikelompokkan berdasarkan kemampuan membaca al-Qur’an, pengetahuan tentang agama Islam secara umum, kemampuan mengenal dan memahami bahasa arab serta pengalaman menjalankan pembelajaran ala pondok pesantren.

    Penjenjangan dikelompokan menjadi 3 jenjang, yaitu Ibtidaiyah, Tsanawiyah, dan Aliyah.
    
    Kelas Ibtidaiyah akan ditempati oleh para santri yang belum lancar membaca Al-Qur’an serta tidak mengenal bahasa arab. Sedangkan Tsanawiyah adalah tingkatan menengah atau rata-rata  dimana santri telah mampu mengenal baca tulis Al-Qur’an namun masih belum terlalu mahir, serta minim kemampuan bahasa arab. Kelas Aliyah akan dihuni para santri yang telah memiliki pengalaman kepesantrenan dengan kemampuan mahir membaca Al qur’an dan  memiliki bekal bahasa Arab yang cukup untuk mengkaji kitab-kitab dan sumber agama yang berbahasa Arab.</p>

